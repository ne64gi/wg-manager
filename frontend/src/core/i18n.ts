export function getUiLocale(): "en" | "ja" {
  if (typeof document === "undefined") {
    return "en";
  }

  return document.documentElement.lang === "ja" ? "ja" : "en";
}

export function t(key: string, fallback: string): string {
  if (getUiLocale() !== "ja") {
    return fallback;
  }

  return JA_TRANSLATIONS[key] ?? fallback;
}

export function translateErrorMessage(message: string): string {
  if (getUiLocale() !== "ja") {
    return message;
  }

  const normalized = message.trim().toLowerCase();
  if (normalized === "invalid username or password") {
    return t("auth.invalid_credentials", "ユーザー名またはパスワードが正しくありません");
  }
  if (normalized === "current password is incorrect") {
    return t("auth.current_password_invalid", "現在のパスワードが正しくありません");
  }
  if (normalized === "initial login user has already been created") {
    return t("auth.setup_exists", "Initial setup has already been completed");
  }

  return message;
}

export function formatApplyFailureMessage(
  action: string,
  errorMessage?: string,
): string {
  const templateKey = errorMessage
    ? "common.apply_failed_after_change"
    : "common.apply_failed_after_change_no_error";
  const fallback = errorMessage
    ? `${action} Change saved, but apply failed: ${errorMessage}`
    : `${action} Change saved, but apply failed.`;

  return t(templateKey, fallback)
    .replace("{action}", action)
    .replace("{error}", errorMessage ?? "");
}
